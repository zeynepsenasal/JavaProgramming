package day03_EcsapeSequences;
// \n: creates a new line
// \t: creates space for paragraph (like tab)
// \": for the "" marks in the text
// \\: for creating \ in the text
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("----------------");
        System.out.println("\tJava is Cool Programming Language");
        System.out.println("-------------------------------------");
        System.out.println("/ \\");
        System.out.println("My favorite TV Show is \"Game of Thrones\"");
    }
}
